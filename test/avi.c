void test_avi_read(ffstr data, int partial)
{
	int r;
	ffstr in = {}, out;
	aviread a = {};
	a.log = avir_log;
	aviread_open(&a);
	ffuint off = 0;
	ffuint track_total = 1;
	ffuint track_audio = 0;
	ffuint itag = 0;
	ffuint frno = 0;

	for (int i = data.len*2;;  i--) {
		x(i >= 0);

		r = aviread_process(&a, &in, &out);
		// xlog("aviread_process: %d", r);
		switch (r) {
		case AVIREAD_HEADER: {
			const struct avi_audio_info *ti = aviread_track_info(&a, track_audio);
			x(NULL == aviread_track_info(&a, track_total));
			xieq(1, ti->type);
			xieq(AVI_A_PCM, ti->codec);
			xieq(16, ti->bits);
			xieq(2, ti->channels);
			xieq(44100, ti->sample_rate);
			x(ti->codec_conf.len == 0);
			goto next;
		}

		case AVIREAD_TAG: {
			ffstr val;
			int t = aviread_tag(&a, &val);
			xlog("aviread_tag: %d = %S", t, &val);
			switch (itag++) {
			case 0:
				xieq(MMTAG_ARTIST, t);
				xseq(&val, "A");
				break;
			case 1:
				xieq(MMTAG_TITLE, t);
				xseq(&val, "T");
				break;
			}
			break;
		}

		case AVIREAD_MORE:
			ffstr_setstr(&in, &data);
			ffstr_shift(&in, off);
			if (partial)
				ffstr_set(&in, data.ptr + off, ffmin(partial, data.len - off));
			off += in.len;
			break;

		default:
			xlog("aviread_process: %s", aviread_error(&a));
			x(0);
		}
	}

next:
	aviread_track_activate(&a, 0);
	for (;;) {
		r = aviread_process(&a, &in, &out);
		// xlog("aviread_process: %d", r);
		switch (r) {
		case AVIREAD_DATA:
			// xseq(&out, "aacframe1");
			frno++;
			xlog("frame:%u curpos=%u", (int)out.len, (int)aviread_cursample(&a));
			break;
		case AVIREAD_DONE:
			goto end;

		case AVIREAD_MORE:
			ffstr_setstr(&in, &data);
			ffstr_shift(&in, off);
			if (partial)
				ffstr_set(&in, data.ptr + off, ffmin(partial, data.len - off));
			off += in.len;
			break;

		default:
			x(0);
		}
	}

	//aviread_seek

end:
	x(frno != 0);
	aviread_close(&a);
}

